class algo():
    ALGO_MAP = {'min-max': minMax}
    AI = ALGO_MAP['min-max']
    depth = 1

    def __init__(self, algo):
        self.AI = self.ALGO_MAP[algo]

    def makeMove(self, gamestate, valid_moves, return_queue):
        return self.AI(gamestate, valid_moves, return_queue)
